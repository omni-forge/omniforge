#!/usr/bin/env python3
"""OmniForge dataset cleaner with quality scoring.

Source: ZIP1 (primary). Filters: length, alphanumeric ratio, line length,
        auto-generated detection, minified detection, quality scoring.
"""

import gzip
import json
import re
import sys
from pathlib import Path
from typing import Dict

import config

AUTOGEN_PATTERNS = [
    r"auto-generated",
    r"autogenerated",
    r"do not edit",
    r"automatically generated",
    r"generated by",
    r"this (file|code) was (auto|automatically) generated",
    r"@generated",
    r"<auto-generated>",
]

MINIFIED_THRESHOLD = 500


def is_auto_generated(text: str) -> bool:
    text_lower = text.lower()
    for pattern in AUTOGEN_PATTERNS:
        if re.search(pattern, text_lower):
            return True
    return False


def is_minified(text: str) -> bool:
    lines = text.split("\n")
    return len(lines) == 1 and len(text) > MINIFIED_THRESHOLD


def compute_quality(text: str) -> float:
    """Compute a quality score from 0 to 1 based on code characteristics."""
    score = 0.0
    lines = text.split("\n")
    total_chars = len(text)

    # Presence of comments (adds 0.2)
    if re.search(r"#|//|/\*|'''|\"\"\"", text):
        score += 0.2

    # Presence of function/class definitions (adds 0.3)
    if re.search(r"(def |class |function |=>)", text):
        score += 0.3

    # Reasonable average line length between 20 and 80 (adds 0.2)
    if lines:
        avg_line_len = total_chars / max(len(lines), 1)
        if 20 <= avg_line_len <= 80:
            score += 0.2

    # Import density above 0.01 (adds 0.1)
    import_lines = sum(1 for line in lines if line.strip().startswith(("import ", "from ", "require(", "#include")))
    import_density = import_lines / max(len(lines), 1)
    if import_density > 0.01:
        score += 0.1

    # Character diversity above 0.3 (adds 0.2)
    if total_chars > 0:
        diversity = len(set(text)) / max(total_chars, 1)
        if diversity > 0.3:
            score += 0.2

    return min(score, 1.0)


def clean_dataset() -> Dict[str, int]:
    """Read raw dataset, apply filters, output clean dataset with quality scores."""
    config.CLEAN_DATA_DIR.mkdir(parents=True, exist_ok=True)

    stats = {
        "total_read": 0,
        "too_short": 0,
        "too_long": 0,
        "low_alnum": 0,
        "avg_line_too_long": 0,
        "auto_generated": 0,
        "minified": 0,
        "low_quality": 0,
        "written": 0,
    }

    with gzip.open(config.RAW_DATASET_PATH, "rt", encoding="utf-8") as fin, \
         gzip.open(config.CLEAN_DATASET_PATH, "wt", encoding="utf-8") as fout:

        for line in fin:
            line = line.strip()
            if not line:
                continue

            try:
                record = json.loads(line)
            except json.JSONDecodeError:
                continue

            stats["total_read"] += 1
            text = record.get("content", "")

            # Normalize line endings
            text = text.replace("\r\n", "\n").replace("\r", "\n")
            # Strip null bytes
            text = text.replace("\x00", "")

            total_chars = len(text)

            # Filter: too short
            if total_chars < config.MIN_RAW_DOCUMENT_CHARS:
                stats["too_short"] += 1
                continue

            # Filter: too long
            if total_chars > config.MAX_RAW_DOCUMENT_CHARS:
                stats["too_long"] += 1
                continue

            # Filter: low alphanumeric ratio
            alphanum = sum(c.isalnum() for c in text)
            alnum_ratio = alphanum / max(total_chars, 1)
            if alnum_ratio < 0.25:
                stats["low_alnum"] += 1
                continue

            # Filter: average line length too high
            lines = text.split("\n")
            avg_line = total_chars / max(len(lines), 1)
            if avg_line > 500:
                stats["avg_line_too_long"] += 1
                continue

            # Filter: auto-generated
            if is_auto_generated(text):
                stats["auto_generated"] += 1
                continue

            # Filter: minified files
            if is_minified(text):
                stats["minified"] += 1
                continue

            # Quality scoring
            quality = compute_quality(text)
            if quality < 0.4:
                stats["low_quality"] += 1
                continue

            record["quality_score"] = round(quality, 4)
            record["content"] = text
            fout.write(json.dumps(record, ensure_ascii=False) + "\n")
            stats["written"] += 1

    return stats


def main() -> None:
    if not config.RAW_DATASET_PATH.exists():
        print(f"[cleaner] ERROR: Raw dataset not found at {config.RAW_DATASET_PATH}")
        print("[cleaner] Run dataset_collector.py first.")
        sys.exit(1)

    stats = clean_dataset()
    print("[cleaner] Dataset cleaning complete.")
    print(f"[cleaner]   Read:           {stats['total_read']:>8,}")
    print(f"[cleaner]   Too short:      {stats['too_short']:>8,}")
    print(f"[cleaner]   Too long:       {stats['too_long']:>8,}")
    print(f"[cleaner]   Low alnum:      {stats['low_alnum']:>8,}")
    print(f"[cleaner]   Avg line long:  {stats['avg_line_too_long']:>8,}")
    print(f"[cleaner]   Auto-generated: {stats['auto_generated']:>8,}")
    print(f"[cleaner]   Minified:       {stats['minified']:>8,}")
    print(f"[cleaner]   Low quality:    {stats['low_quality']:>8,}")
    print(f"[cleaner]   Written:        {stats['written']:>8,}")


if __name__ == "__main__":
    main()